from __future__ import annotations

from textwrap import dedent

from .models import (
    AgentStatus,
    ArtifactType,
    BuilderState,
    GeneratedArtifact,
    ReviewFeedback,
    TestResult,
    VerificationResult,
)
from .settings import get_settings


def _add_decision(state: BuilderState, agent: str, action: str, reason: str) -> None:
    state.current_agent = agent
    state.decision_history.append({"agent": agent, "action": action, "reason": reason})
    state.touch()


def planner_architect(state: BuilderState) -> BuilderState:
    _add_decision(state, "planner_architect", "plan", "Create production-ready plan")
    state.status = AgentStatus.IN_PROGRESS
    state.plan = dedent(
        f"""
        # Projektplan: {state.project_id}

        ## Ziel
        Aus der Anfrage wird ein lauffähiges LangGraph-Projekt generiert, getestet,
        reviewed und mit Docker/K3s deploybar gemacht.

        ## Erfolgskriterien
        - Pydantic-State und typisierte Artefakte sind vorhanden.
        - Supervisor-Orchestration führt Planner, Builder, Memory Designer,
          Executor, Reviewer, Verifier und Deployment Specialist aus.
        - Tests und Qualitätsbewertung laufen deterministisch.
        - Deployment kann per Docker Compose auf einem VPS gestartet werden.

        ## Risiken
        - LLM-Ausgaben koennen inkonsistent sein; strukturierte Modelle und Reviewer
          reduzieren das Risiko.
        - Sandbox-Ausfuehrung braucht klare Allow-Lists.
        - Produktion braucht echte Secrets und persistente Volumes.
        """
    ).strip()
    state.architecture_decisions.extend(
        [
            "Use LangGraph StateGraph for orchestration.",
            "Use Pydantic models as the contract between agents.",
            "Use deterministic node implementations for testability; add LLM adapters later.",
            "Use Docker Compose as the default VPS deployment path.",
        ]
    )
    state.human_approval_points.append("Approve project plan before production rollout")
    return state


def agent_builder(state: BuilderState) -> BuilderState:
    _add_decision(state, "agent_builder", "generate_code", "Create minimal runnable artifact set")
    state.current_code = dedent(
        """
        from langgraph.graph import END, StateGraph

        def build_graph():
            graph = StateGraph(dict)
            graph.add_node("start", lambda state: {**state, "built": True})
            graph.set_entry_point("start")
            graph.add_edge("start", END)
            return graph.compile()
        """
    ).strip()
    state.generated_artifacts.append(
        GeneratedArtifact(
            filename="graph.py",
            content=state.current_code,
            artifact_type=ArtifactType.CODE,
            description="Minimal LangGraph project graph generated by the Agent Builder.",
        )
    )
    state.generated_artifacts.append(
        GeneratedArtifact(
            filename="state.py",
            content=(
                "from pydantic import BaseModel\n\n"
                "class ProjectState(BaseModel):\n"
                "    built: bool = False\n"
            ),
            artifact_type=ArtifactType.CODE,
            description="Minimal generated project state model.",
        )
    )
    return state


